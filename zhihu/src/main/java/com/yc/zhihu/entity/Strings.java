package com.yc.zhihu.entity;

public class Strings {

	private String ids;
	private String kind;
	
	
	public Strings() {}
	public Strings(String ids, String kind) {
		this.ids = ids;
		this.kind = kind;
	}
	public String getIds() {
		return ids;
	}
	public void setIds(String ids) {
		this.ids = ids;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	
	
}
